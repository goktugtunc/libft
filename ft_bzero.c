#include "libft.h"

void	deneme(char *s, int n)
{
	int     i;

        i = 0;
        while (i < n)
        {
                s[i] = '\0';
                i++;
        }
}


void ft_bzero(void *s, size_t n)
{
	deneme(s, n);
}
